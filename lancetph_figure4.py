def edge_key(a: str, b: str) -> Tuple[str, str]:
    return tuple(sorted([str(a), str(b)]))


def safe_pearson(x: pd.Series, y: pd.Series) -> Tuple[float, float]:
    x = pd.to_numeric(x, errors="coerce")
    y = pd.to_numeric(y, errors="coerce")
    mask = x.notna() & y.notna()
    x = x[mask]
    y = y[mask]
    if len(x) < 3 or x.nunique() <= 1 or y.nunique() <= 1:
        return np.nan, np.nan
    return pearsonr(x, y)


def make_edge_table(
    df: pd.DataFrame,
    years: Iterable[int],
    vars_: List[str] = VARS,
    p_cut: float = P_CUT,
    r_cut: float = R_CUT,
) -> pd.DataFrame:
    """Create significant correlation edge table for one time window."""
    sub = df.loc[df["year"].isin(years), vars_].copy()
    sub = sub.apply(pd.to_numeric, errors="coerce")

    rows = []
    for a, b in combinations(vars_, 2):
        r, p = safe_pearson(sub[a], sub[b])
        if pd.notna(r) and pd.notna(p) and p < p_cut and abs(r) >= r_cut:
            rows.append({
                "from": a,
                "to": b,
                "r": float(r),
                "p": float(p),
                "abs_r": float(abs(r)),
                "n": int(sub[[a, b]].dropna().shape[0]),
            })
    return pd.DataFrame(rows)


def compare_edges(early: pd.DataFrame, late: pd.DataFrame) -> pd.DataFrame:
    """Classify edges into persistent, early_only, late_only."""
    e0 = early.copy()
    e1 = late.copy()
    if e0.empty:
        e0 = pd.DataFrame(columns=["from", "to", "r", "p", "abs_r", "n"])
    if e1.empty:
        e1 = pd.DataFrame(columns=["from", "to", "r", "p", "abs_r", "n"])

    e0["key"] = [edge_key(a, b) for a, b in zip(e0["from"], e0["to"])]
    e1["key"] = [edge_key(a, b) for a, b in zip(e1["from"], e1["to"])]

    all_keys = sorted(set(e0["key"]).union(set(e1["key"])))

    rows = []
    e0_map = e0.set_index("key").to_dict(orient="index") if not e0.empty else {}
    e1_map = e1.set_index("key").to_dict(orient="index") if not e1.empty else {}

    for k in all_keys:
        in0 = k in e0_map
        in1 = k in e1_map
        if in0 and in1:
            status = "persistent"
        elif in0 and not in1:
            status = "early_only"
        else:
            status = "late_only"

        a, b = k
        r_early = e0_map.get(k, {}).get("r", np.nan)
        r_late = e1_map.get(k, {}).get("r", np.nan)

        rows.append({
            "from": a,
            "to": b,
            "status": status,
            "r_early": r_early,
            "r_late": r_late,
            "r_plot_early": r_early if in0 else np.nan,
            "r_plot_late": r_late if in1 else np.nan,
            "abs_r_plot_early": abs(r_early) if in0 and pd.notna(r_early) else np.nan,
            "abs_r_plot_late": abs(r_late) if in1 and pd.notna(r_late) else np.nan,
        })

    return pd.DataFrame(rows)


def network_summary(edge_status: pd.DataFrame, early: pd.DataFrame, late: pd.DataFrame) -> Dict[str, float]:
    """Calculate density, mean absolute association, and persistent percentage."""
    possible_edges = len(VARS) * (len(VARS) - 1) / 2

    early_n = len(early)
    late_n = len(late)
    early_density = early_n / possible_edges
    late_density = late_n / possible_edges

    early_mean = early["abs_r"].mean() if not early.empty else np.nan
    late_mean = late["abs_r"].mean() if not late.empty else np.nan

    persistent_n = int((edge_status["status"] == "persistent").sum()) if not edge_status.empty else 0
    union_n = int(len(edge_status))
    persistent_pct = 100 * persistent_n / union_n if union_n > 0 else np.nan

    return {
        "early_edges": early_n,
        "late_edges": late_n,
        "early_density": early_density,
        "late_density": late_density,
        "early_mean_abs_r": early_mean,
        "late_mean_abs_r": late_mean,
        "persistent_edges": persistent_n,
        "union_edges": union_n,
        "persistent_pct": persistent_pct,
    }


def node_degrees(edge_status: pd.DataFrame, period: str) -> Dict[str, int]:
    """Degree used for node size in early or late panel."""
    if edge_status.empty:
        return {v: 0 for v in VARS}

    if period == "early":
        edges = edge_status.loc[edge_status["r_plot_early"].notna()]
    else:
        edges = edge_status.loc[edge_status["r_plot_late"].notna()]

    deg = {v: 0 for v in VARS}
    for _, row in edges.iterrows():
        deg[row["from"]] += 1
        deg[row["to"]] += 1
    return deg


def draw_network(ax, edge_status: pd.DataFrame, period: str, title: str):
    """Draw early or late network panel."""
    ax.set_title(title, fontsize=9, fontstyle="italic")
    ax.axis("off")

    deg = node_degrees(edge_status, period)

    # Draw edges.
    for _, row in edge_status.iterrows():
        if period == "early" and pd.isna(row["r_plot_early"]):
            continue
        if period == "late" and pd.isna(row["r_plot_late"]):
            continue

        a, b = row["from"], row["to"]
        x0, y0 = FIXED_POS[a]
        x1, y1 = FIXED_POS[b]

        if row["status"] == "persistent":
            color = EDGE_COLORS["persistent"]
            alpha = 0.72
        elif row["status"] == "early_only":
            color = EDGE_COLORS["early_only"] if period == "early" else EDGE_COLORS["late_only"]
            alpha = 0.65 if period == "early" else 0.0
        else:
            color = EDGE_COLORS["late_only"] if period == "late" else EDGE_COLORS["early_only"]
            alpha = 0.80 if period == "late" else 0.0

        if alpha == 0:
            continue

        abs_r = row["abs_r_plot_early"] if period == "early" else row["abs_r_plot_late"]
        width = 0.4 + 2.6 * min(float(abs_r), 1.0)

        ax.plot([x0, x1], [y0, y1], color=color, alpha=alpha, linewidth=width, zorder=1)

    max_deg = max(max(deg.values()), 1)

    # Draw nodes.
    for node in VARS:
        x, y = FIXED_POS[node]
        dom = DOMAIN[node]
        color = NODE_COLORS[dom]
        size = 45 + 260 * (deg[node] / max_deg)

        ax.scatter(
            x, y,
            s=size,
            color=color,
            edgecolor="white",
            linewidth=0.7,
            alpha=0.92,
            zorder=3,
        )

        label = DISPLAY_LABELS.get(node, node)
        label_color = "#B0B0B0"
        fontweight = "bold"

        # Highlight visible nodes in current network.
        if deg[node] > 0:
            if dom == "Diet":
                label_color = "#E41A1C"
            elif dom == "Disease":
                label_color = "#0046D5"
            else:
                label_color = "#0046D5" if node in ["PR", "RH", "Heat_index", "MeanT", "PM25"] else "#222222"

        ax.text(
            x, y + 0.045,
            label,
            fontsize=7.2,
            color=label_color,
            ha="center",
            va="bottom",
            fontweight=fontweight,
            zorder=4,
        )

    ax.set_xlim(-0.10, 1.12)
    ax.set_ylim(-0.08, 1.03)


def draw_summary_box(ax, summary: Dict[str, float]):
    """Draw center summary box between early and late networks."""
    ax.axis("off")
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)

    # background gradient-like blocks
    ax.axvspan(0.00, 0.50, color="#dbe9ff", alpha=0.75)
    ax.axvspan(0.50, 1.00, color="#ffe3dc", alpha=0.75)

    def fmt2(x):
        return "NA" if pd.isna(x) else f"{x:.2f}"

    def fmt0(x):
        return "NA" if pd.isna(x) else f"{x:.0f}"

    lines = [
        ("Density", fmt2(summary["early_density"]), fmt2(summary["late_density"])),
        ("Mean", fmt2(summary["early_mean_abs_r"]), fmt2(summary["late_mean_abs_r"])),
        ("Persistent", fmt0(summary["persistent_pct"]) + "%", ""),
    ]

    y_positions = [0.76, 0.51, 0.26]
    for (label, left, right), y in zip(lines, y_positions):
        ax.text(0.20, y, left, ha="center", va="center", fontsize=8, fontweight="bold")
        ax.annotate(
            "",
            xy=(0.72, y), xytext=(0.32, y),
            arrowprops=dict(arrowstyle="simple", color="black", lw=0.7),
        )
        if right:
            ax.text(0.82, y, right, ha="center", va="center", fontsize=8, fontweight="bold")
        ax.text(0.50, y - 0.10, label, ha="center", va="center", fontsize=8, fontweight="bold")


def make_cluster_networks(data: pd.DataFrame):
    """Create all edge status and summary tables."""
    required = ["year", "Climate_cluster_5"] + VARS
    missing = [c for c in required if c not in data.columns]
    if missing:
        raise KeyError(f"Missing columns: {missing}")

    data = data.copy()
    data["year"] = pd.to_numeric(data["year"], errors="coerce")
    data["Climate_cluster_5"] = pd.to_numeric(data["Climate_cluster_5"], errors="coerce")

    edge_tables = []
    summary_rows = []

    for cl in CLUSTERS:
        df_cl = data.loc[data["Climate_cluster_5"] == cl].copy()

        early = make_edge_table(df_cl, EARLY_YEARS)
        late = make_edge_table(df_cl, LATE_YEARS)
        status = compare_edges(early, late)

        if not status.empty:
            status.insert(0, "cluster", f"C{cl}")
            edge_tables.append(status)

        summ = network_summary(status, early, late)
        summ["cluster"] = f"C{cl}"
        summary_rows.append(summ)

    edge_all = pd.concat(edge_tables, ignore_index=True) if edge_tables else pd.DataFrame()
    summary_all = pd.DataFrame(summary_rows)

    return edge_all, summary_all


