def read_csv_safely(path: Path) -> pd.DataFrame:
    """
    Read CSV using common encodings.
    """
    encodings = ["utf-8-sig", "utf-8", "cp949", "euc-kr", "latin1"]
    last_error = None

    for enc in encodings:
        try:
            return pd.read_csv(path, encoding=enc)
        except UnicodeDecodeError as exc:
            last_error = exc

    raise RuntimeError(f"Could not read {path}. Last error: {last_error}")


def ensure_required_columns(df: pd.DataFrame, cols: List[str], label: str = "data") -> None:
    """
    Raise a clear error if required columns are missing.
    """
    missing = [c for c in cols if c not in df.columns]
    if missing:
        raise KeyError(f"Missing columns in {label}: {missing}")


def node_domain(v: str) -> str:
    """
    Return domain label for a node.
    """
    if v.startswith("GDD"):
        return "Diet"
    if v.startswith("GBD"):
        return "Disease"
    return "Climate"


def pair_type(a: str, b: str) -> str:
    """
    Return pair type such as Diet-Disease, Climate-Disease, etc.
    """
    da = node_domain(a)
    db = node_domain(b)

    if da == db:
        return f"{da}-{db}"

    return "-".join(sorted([da, db]))


def is_cross_domain_pair(a: str, b: str) -> bool:
    """
    Keep only pairs from different domains.
    """
    return node_domain(a) != node_domain(b)


def get_cross_domain_pairs(vars_: List[str] = VARS) -> List[Tuple[str, str]]:
    """
    Return all cross-domain variable pairs.
    """
    pairs = []

    for a, b in combinations(vars_, 2):
        if is_cross_domain_pair(a, b):
            pairs.append((a, b))

    return pairs


def safe_pearson_abs(x: pd.Series, y: pd.Series) -> Tuple[float, float, int]:
    """
    Safely calculate Pearson r and return |r|, r, and pairwise complete n.
    """
    x = pd.to_numeric(x, errors="coerce")
    y = pd.to_numeric(y, errors="coerce")

    mask = x.notna() & y.notna()
    x_use = x[mask]
    y_use = y[mask]
    n = int(mask.sum())

    if n < MIN_N_COUNTRIES:
        return np.nan, np.nan, n

    if x_use.nunique(dropna=True) <= 1 or y_use.nunique(dropna=True) <= 1:
        return np.nan, np.nan, n

    r, _ = pearsonr(x_use, y_use)

    if not np.isfinite(r):
        return np.nan, np.nan, n

    return abs(float(r)), float(r), n


def compute_pair_level_coupling(
    df_subset: pd.DataFrame,
    cluster_num: int,
    year: int,
    pairs: List[Tuple[str, str]],
) -> pd.DataFrame:
    """
    Calculate pair-level Pearson correlations for one cluster-year subset.
    """
    rows = []

    for a, b in pairs:
        abs_r, r, n_pair = safe_pearson_abs(df_subset[a], df_subset[b])

        rows.append({
            "cluster": f"C{cluster_num}",
            "cluster_num": cluster_num,
            "cluster_name": CLUSTER_FULL_NAMES[cluster_num],
            "year": year,
            "from": a,
            "to": b,
            "from_domain": node_domain(a),
            "to_domain": node_domain(b),
            "pair_type": pair_type(a, b),
            "r": r,
            "abs_r": abs_r,
            "n_pair_complete": n_pair,
        })

    return pd.DataFrame(rows)


def summarize_coupling(pair_tbl: pd.DataFrame) -> Dict[str, float]:
    """
    Summarize cross-domain pair-level |r| values.
    """
    vals = pd.to_numeric(pair_tbl["abs_r"], errors="coerce")
    vals = vals[np.isfinite(vals)]

    if len(vals) == 0:
        return {
            "mean_abs_r": np.nan,
            "median_abs_r": np.nan,
            "max_abs_r": np.nan,
            "n_pairs_total": int(len(pair_tbl)),
            "n_pairs_valid": 0,
        }

    return {
        "mean_abs_r": float(vals.mean()),
        "median_abs_r": float(vals.median()),
        "max_abs_r": float(vals.max()),
        "n_pairs_total": int(len(pair_tbl)),
        "n_pairs_valid": int(vals.shape[0]),
    }


def compute_cross_domain_coupling_tables(data: pd.DataFrame):
    """
    Compute cross-domain network coupling strength by climate cluster and year.

    Returns
    -------
    summary_table:
        One row per cluster-year.

    pair_level_table:
        One row per cluster-year-variable pair.
    """
    required = ["year", "Climate_cluster_5"] + VARS
    ensure_required_columns(data, required, "data_merged_quarters.csv")

    df = data.copy()
    df["year"] = pd.to_numeric(df["year"], errors="coerce")
    df["Climate_cluster_5"] = pd.to_numeric(df["Climate_cluster_5"], errors="coerce")

    pairs = get_cross_domain_pairs(VARS)

    summary_rows = []
    pair_tables = []

    for cl in CLUSTERS:
        for yr in YEARS:
            sub = df.loc[
                (df["Climate_cluster_5"] == cl)
                & (df["year"] == yr),
                VARS
            ].copy()

            n_countries = int(sub.dropna(how="all").shape[0])

            pair_tbl = compute_pair_level_coupling(
                df_subset=sub,
                cluster_num=cl,
                year=yr,
                pairs=pairs,
            )
            pair_tables.append(pair_tbl)

            stats = summarize_coupling(pair_tbl)

            summary_rows.append({
                "cluster": f"C{cl}",
                "cluster_num": cl,
                "cluster_name": CLUSTER_FULL_NAMES[cl],
                "year": yr,
                "n_countries": n_countries,
                "coupling_mode": COUPLING_MODE,
                **stats,
            })

    summary_table = pd.DataFrame(summary_rows)
    pair_level_table = pd.concat(pair_tables, ignore_index=True)

    return summary_table, pair_level_table


