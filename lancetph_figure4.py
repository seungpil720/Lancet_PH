def node_domain(v: str) -> str:
    if v.startswith("GDD"):
        return "Diet"
    if v.startswith("GBD"):
        return "Disease"
    return "Climate"


def pair_allowed(a: str, b: str, mode: str = COUPLING_MODE) -> bool:
    da = node_domain(a)
    db = node_domain(b)

    if mode == "all_edges":
        return True

    if mode == "cross_domain_only":
        return da != db

    if mode == "diet_disease_climate_only":
        pair = {da, db}
        return pair in [
            {"Diet", "Disease"},
            {"Diet", "Climate"},
            {"Disease", "Climate"},
        ]

    raise ValueError(f"Unknown COUPLING_MODE: {mode}")


def safe_pearson_abs(x: pd.Series, y: pd.Series) -> float:
    x = pd.to_numeric(x, errors="coerce")
    y = pd.to_numeric(y, errors="coerce")
    mask = x.notna() & y.notna()
    x = x[mask]
    y = y[mask]

    if len(x) < 3 or x.nunique() <= 1 or y.nunique() <= 1:
        return np.nan

    r, _ = pearsonr(x, y)
    return abs(r) if np.isfinite(r) else np.nan


def coupling_strength_for_subset(df: pd.DataFrame, vars_: List[str] = VARS, mode: str = COUPLING_MODE) -> Dict[str, float]:
    """Calculate mean absolute Pearson r for selected pairs."""
    values = []

    for a, b in combinations(vars_, 2):
        if not pair_allowed(a, b, mode):
            continue
        values.append(safe_pearson_abs(df[a], df[b]))

    values = np.array(values, dtype=float)
    values = values[np.isfinite(values)]

    if len(values) == 0:
        return {
            "mean_abs_r": np.nan,
            "median_abs_r": np.nan,
            "max_abs_r": np.nan,
            "n_pairs_valid": 0,
        }

    return {
        "mean_abs_r": float(np.mean(values)),
        "median_abs_r": float(np.median(values)),
        "max_abs_r": float(np.max(values)),
        "n_pairs_valid": int(len(values)),
    }


